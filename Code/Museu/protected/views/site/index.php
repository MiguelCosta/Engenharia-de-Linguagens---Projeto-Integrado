<p>&nbsp;&nbsp;&nbsp;&nbsp;Inscreve as suas principais finalidades na
	perspectiva do conhecimento da emigração portuguesa, nela incluindo o
	fenómeno que se verificou para a África, bem como aquele que se
	verifica nos nossos dias, detendo-se, particularmente:</p>
<ul>
	<li>na emigração para o Brasil (séc. XIX e primeiras décadas do XX)</li>
	<li>na emigração para os países Europeus (2.ª metade do séc.XX)</li>
</ul>

<p>&nbsp;&nbsp;&nbsp;&nbsp;Funda a sua existência no facto da mobilidade
	geográfica constituir um fenómeno estrutural da sociedade portuguesa,
	deixando marcas em todos os continentes, não excluindo a emergência da
	imigração como uma nova expressão de mobilidade em Portugal.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;Por outro lado, o fenómeno da mobilidade
	humana constituiu-se como a expressão mais significativa da
	transformação social e cultural da humanidade, fazendo dos actores
	migrantes os principais motores da história da civilização, onde se
	experimentam, ao mesmo tempo, a tragédia e a grandeza humana.</p>
