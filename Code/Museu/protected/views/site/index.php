			<p>&nbsp;&nbsp;&nbsp;&nbsp;Um Museu de âmbito nacional constituído
				por Núcleos Museológicos e Sítios Históricos da Emigração e do
				Retorno, instalados em espaços físicos temáticos.</p>

			<p>&nbsp;&nbsp;&nbsp;&nbsp;Estes organizam um museu polinucleado e um
				itinerário desenhado para a valorização de espólios e das memórias
				associadas às migrações.</p>

			<p>&nbsp;&nbsp;&nbsp;&nbsp;Nestes lugares e sítios encontram-se os
				acervos documentais e museológicos de cada um dos pólos, dando aos
				contextos de origem a compreensão dos factores de emigração.</p>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;Neles se evidenciam os quadros de risco
				inscritos nos processo da viagem como sendo o momento da ruptura
				cultural.</p>
